use super::{name, Name};
use crate::sname;
use educe::Educe;
use std::array::from_fn;

/// Encoding for Type1 and other font types except Type0, which allows
/// char code larger than u8.
/// map char code (u8) to glyph name
#[derive(Debug, PartialEq, Clone, Educe)]
#[educe(Deref, DerefMut)]
pub struct Encoding([Name; 256]);

impl Encoding {
    #[inline]
    pub const fn new(names: [Name; 256]) -> Self {
        Self(names)
    }

    #[inline]
    pub fn from(names: [&str; 256]) -> Self {
        Self(from_fn(|i| name(names[i])))
    }

    #[inline]
    pub fn get_name(&self, ch: u8) -> &Name {
        &self.0[ch as usize]
    }

    /// Get glyph name for char code.
    #[inline]
    pub fn get_str(&self, ch: u8) -> &str {
        self.0[ch as usize].as_str()
    }

    pub fn predefined(name: Name) -> Option<Self> {
        if name == sname("MacRomanEncoding") {
            Some(Self::MAC_ROMAN)
        } else if name == sname("MacExpertEncoding") {
            Some(Self::MAC_EXPERT)
        } else if name == sname("WinAnsiEncoding") {
            Some(Self::WIN_ANSI)
        } else if name == sname("StandardEncoding") {
            Some(Self::STANDARD)
        } else if name == sname("Symbol") {
            Some(Self::SYMBOL)
        } else if name == sname("ZapfDingbats") {
            Some(Self::ZAPFDINGBATS)
        } else {
            None
        }
    }
}

